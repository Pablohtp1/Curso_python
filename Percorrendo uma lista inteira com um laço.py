# Com frequência, você vai querer percorrrer todas as entradas de uma lista, executando a mesma tarefa em cada item.
# Usar um laço "for" evita esses dois problemas ao permitir que o python administr essas questões internamente.
# Aqui está um exemplo de como você pode usar um laço "for" para exibir cada item em uma lista:

magicians = ['alice', 'david', 'carolina']
for magician in magicians:
    print(magician)

# Começamos definindo uma lista 1, exatamente como fizemos anteriormente. Em 2 definimos um laço for.
# Essa linha diz a Python para extrair um nome da lista magicians e armazená-lo na variável magician. Em 3 dizemos a Python para exibir o nome que acabou de ser armazenado em magician.
# O interpretador então repete as linhas 2 e 3, umas vez para cada nome da lista. Ler esse código como para todo mágico na lista de mágicos.
