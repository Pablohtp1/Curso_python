# A função range() de Python facilita gerar uma série de números. Por exemplo, podemos usar a função range() para exibir uma sequência de números, assim:

for value in range(1,5):
    print(value)

# Nesse exemplo, range() exibe apenas os números de 1 a 4. Esse é o outro resultado do comportamento deslocado de um que veremos com frequencia nas linguagens de programação. A função range() faz Python começar a contar no primeiro valor que você lhe fornecer e para quando atingir o segundo valor especificado.
# Como ele para nesse segundo valor, a saída não conterá o valor final, que seria 5, nesse caso. Para exibir os números de 1 a 5, você deve usar range.

for value in range(1,6):
    print(value)

# Para exibir os números de 1 a 5, você deve usar o range(1,6).