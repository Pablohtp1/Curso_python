# Python chama qualquer número com um ponto decimal de número de ponto flutuante (float). Esse termo é usado na maioria das linguagens de programação e se refere-se ao fato de um ponto decimal pode aparecer em qualquer posição em um número.
# Toda linguagem de programação deve ser cuidadosamente projetada para lidar de modo adequado com o números decimais de modo que eles se comportem de forma apropriada, não importa em que lugar o ponto decimal apareca.
# Na maioria das ocasiões, podemos usar decimais sem nos preocupar com o modo como eles se comportam. Basta fornecer os números que você quer usar, e Python provavelmente fará o que você espera >>>

print (0.1 + 0.1)
print (0.2 + 0.2)
print (2 * 0.1)
print (2 * 0.2)

# No entanto, tome cuidado, pois, ás vezes, você poderá obter um número arbitrário de casas decimais em sua resposta.
# Isso acontece com a maioria das linguagens de programação, e não é um erro em Python. É uma consequência de como os computadores armazenam números de ponto flutuante.

print (0.2 + 0.1)
print (3 * 0.1)

# Isso acontece em todas as linguagens de programação, não é mótivo para muita preocupação. Python tenta encontrar uma forma de representar o resultado com precisão, mas é importante estar ciente de que pode haver uma pequena imprecisão ao trabalhar com números de ponto flutuante.