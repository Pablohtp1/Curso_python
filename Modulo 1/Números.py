# Os números são usados com muita frequência em programação para armazenar pontuações em jogos, representa dados em visualizações e em muitas outras tarefas. Python trata os números de várias maneiras diferentes, dependendo de como são usados. Vamos começar aprendendo sobre os diferentes tipos de números com os quais você pode trabalhar em Python.

# Inteiros
# Você pode somar (+), subtrair (-), multiplicar (*) e dividir (/) inteiros em Python.
print (2 + 3)
print (3 - 2)
print (2 * 3)
print (3 / 2)

# Em uma sessão de terminal, python simplesmente devolve o resultado da operação. Dois simbolos de multiplacação são usados em python para representar exponenciação.

print (3 ** 2)
print (3 ** 3)
print (10 ** 6)

# A linguagem Python também aceita a ordem das operações, portante você pode fazer várias operações em uma expressão. Também podemos usar parênteses para modificar a ordem de operações para que o python possa avaliar sua expressão na ordem que vocÊ especificar. Por exemplo: 
print (2 + 3 * 4)
print ((2 + 3) * 4)
